# Example 1: Simple if/else
age = 18
if age >= 18:
    print("You are eligible to vote")
else:
    print("You are not eligible to vote yet")


# Example 2: if/elif/else
grade = 85
if grade >= 90:
    print("A")
elif grade >= 80:
    print("B")
elif grade >= 70:
    print("C")
else:
    print("D")


# Example 3: While loop
count = 0
while count < 5:
    print(f"Count is: {count}")
    count += 1